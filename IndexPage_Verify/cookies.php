<script>
    var openSections = [];

    function toggleSection(index) {
        var body = document.getElementById('body-' + index);
        var chevron = document.getElementById('chevron-' + index);
        var isOpen = openSections.indexOf(index) !== -1;

        if (isOpen) {
            openSections = openSections.filter(function(i){ return i !== index; });
            body.classList.remove('open');
            chevron.innerHTML = '<?php echo addslashes(getIcon('chevron-down', 20, 'currentColor')); ?>';
        } else {
            openSections.push(index);
            body.classList.add('open');
            chevron.innerHTML = '<?php echo addslashes(getIcon('chevron-up', 20, 'currentColor')); ?>';
        }
    }
</script>

